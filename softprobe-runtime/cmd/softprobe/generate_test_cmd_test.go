package main

import (
	"bytes"
	"encoding/json"
	"os"
	"path/filepath"
	"strings"
	"testing"
)

const goldenCasePath = "../../../spec/examples/cases/fragment-happy-path.case.json"

func runGenerateTestCmd(t *testing.T, framework, outPath string) (string, int) {
	t.Helper()
	var stdout, stderr bytes.Buffer
	code := run([]string{
		"generate", "test",
		"--case", goldenCasePath,
		"--framework", framework,
		"--out", outPath,
	}, &stdout, &stderr)
	if code != 0 {
		t.Fatalf("generate test --framework %s exit = %d, stderr=%q", framework, code, stderr.String())
	}
	b, err := os.ReadFile(outPath)
	if err != nil {
		t.Fatalf("read generated file: %v", err)
	}
	return string(b), code
}

func TestGenerateTestJestEmitsFullTestFile(t *testing.T) {
	out := filepath.Join(t.TempDir(), "checkout.test.ts")
	src, _ := runGenerateTestCmd(t, "jest", out)
	for _, want := range []string{
		"describe(",
		"it(",
		"import { Softprobe } from '@softprobe/softprobe-js'",
		"session.findInCase(",
		"session.mockOutbound(",
		"Generated by `softprobe generate test`",
	} {
		if !strings.Contains(src, want) {
			t.Fatalf("jest output missing %q; got:\n%s", want, src)
		}
	}
}

func TestGenerateTestVitestUsesVitestImports(t *testing.T) {
	out := filepath.Join(t.TempDir(), "checkout.test.ts")
	src, _ := runGenerateTestCmd(t, "vitest", out)
	if !strings.Contains(src, "from 'vitest'") {
		t.Fatalf("vitest output missing vitest import:\n%s", src)
	}
	if !strings.Contains(src, "describe(") || !strings.Contains(src, "it(") {
		t.Fatalf("vitest output missing describe/it:\n%s", src)
	}
}

