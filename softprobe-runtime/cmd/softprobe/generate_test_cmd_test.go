func TestGenerateTestUnknownFrameworkFailsWithInvalidArgs(t *testing.T) {
	out := filepath.Join(t.TempDir(), "x.test.ts")
	var stdout, stderr bytes.Buffer
	code := run([]string{
		"generate", "test",
		"--case", goldenCasePath,
		"--framework", "cypress",
		"--out", out,
	}, &stdout, &stderr)
	if code != exitInvalidArgs {
		t.Fatalf("exit = %d, want %d for unknown framework", code, exitInvalidArgs)
	}
}

func TestGenerateTestJSONEnvelopeReportsOutput(t *testing.T) {
	out := filepath.Join(t.TempDir(), "checkout.test.ts")
	var stdout, stderr bytes.Buffer
	code := run([]string{
		"generate", "test",
		"--case", goldenCasePath,
		"--framework", "jest",
		"--out", out,
		"--json",
	}, &stdout, &stderr)
	if code != 0 {
		t.Fatalf("exit = %d, stderr=%q", code, stderr.String())
	}
	var resp struct {
		Status       string `json:"status"`
		ExitCode     int    `json:"exitCode"`
		OutputPath   string `json:"outputPath"`
		Framework    string `json:"framework"`
		RulesEmitted int    `json:"rulesEmitted"`
		CaseID       string `json:"caseId"`
	}
	if err := json.Unmarshal(stdout.Bytes(), &resp); err != nil {
		t.Fatalf("unmarshal: %v; raw=%s", err, stdout.String())
	}
	if resp.Status != "ok" || resp.Framework != "jest" {
		t.Fatalf("resp = %+v", resp)
	}
	if resp.OutputPath != out {
		t.Fatalf("outputPath = %q, want %q", resp.OutputPath, out)
	}
	if resp.CaseID == "" {
		t.Fatal("caseId empty")
	}
}
